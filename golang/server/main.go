// Command awareness-graph is the gRPC service entry point.
//
// Scope (Phase 2 step 7):
//
//   - Flags: -addr (gRPC listen address), -oxigraph-url (SPARQL query
//     endpoint), -require-store (fail-fast on backend health failure).
//   - Construct an Oxigraph client at startup.
//   - Run a one-shot health check; honor -require-store for whether a
//     failure is fatal.
//   - Build the gRPC server with the store reference held on the
//     concrete server struct so future RPC implementations can reach it.
//   - Register the AwarenessGraphServer with Resolve + Impact + Briefing + Query implemented.
//   - Graceful shutdown on SIGINT/SIGTERM; Close() the store last so
//     queries in flight aren't yanked from under their handlers.
//
// Out of scope (deliberately deferred):
//
//   - Arbitrary SPARQL passthrough.
//
//   - Periodic re-checks / readiness probes. One health check at
//     startup, no background loop. Re-checks land if a future operator
//     surface demands them.
